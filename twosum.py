''' Leet Code'''
'''
ลูปแรก ( for i in range(len(nums))) ใช้ในการเลือกตัวเลขตัวแรกจาก array.
ลูปที่สอง ( for j in range(i + 1, len(nums))) ใช้ในการเลือกตัวเลขตัวที่สอง โดยเริ่มจากตัวเลขที่อยู่ถัดจากตัวแรก.
ในแต่ละการวนลูป ตรวจสอบว่าผลรวมของ nums[i] + nums[j] เท่ากับค่าเป้าหมาย ( target) หรือไม่.
ถ้าใช่, คืนค่าดัชนี [i, j] ของตัวเลขทั้งสอง.
'''

def two_sum(nums, target):
    # วนลูปผ่านตัวเลขใน array เพื่อเลือกตัวเลขตัวแรก
    for i in range(len(nums)):
        # วนลูปผ่านตัวเลขถัดมาจากตัวเลขที่เลือกในลูปแรก
        for j in range(i + 1, len(nums)):
            # ตรวจสอบว่าผลรวมของตัวเลขสองตัวเท่ากับค่าเป้าหมายหรือไม่
            if nums[i] + nums[j] == target:
                return [i, j]

# ตัวอย่างการใช้งาน
nums = [2, 7, 11, 15]
target = 9
result = two_sum(nums, target)
print(result)  # ผลลัพธ์ที่ได้คือ [0, 1]